public class ContadorWhile {
    public static void main(String[] args) {
        // PROGRAMA QUE MUESTRE POR PANTALLA LOS 20 PRIMEMROS NÃšMEROS NATURALES   

        // CONTADOR - SI
        // ACUMULADOR - NO
        // INTERRUPTOR - NO

        int cont = 1;

        while (cont < 21) { //DIFERENTE !=, (MENOR QUE) <21, (MENOR O IGUAL QUE) <=20
            // INSTRUCCIONES
            System.out.print(cont+ " ");
            cont++;
        }
        System.out.println(""); // CUANDO ACABE SALTA
    }

}
